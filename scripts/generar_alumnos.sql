-- Limpiar datos relacionados con alumnos (excepto 20224106A)
-- Primero obtener el ID del alumno a preservar
DO $$
DECLARE
    alumno_preservado_id INT;
BEGIN
    -- Obtener el ID del alumno 20224106A si existe
    SELECT id INTO alumno_preservado_id FROM alumno WHERE codigo = '20224106A';
    
    -- Eliminar registros relacionados de otros alumnos
    IF alumno_preservado_id IS NOT NULL THEN
        DELETE FROM log_ciclo_relativo WHERE alumno_id != alumno_preservado_id;
        DELETE FROM log_creditos WHERE alumno_id != alumno_preservado_id;
        DELETE FROM matricula WHERE alumno_id != alumno_preservado_id;
        DELETE FROM usuario WHERE alumno_id IS NOT NULL AND alumno_id != alumno_preservado_id;
        DELETE FROM alumno WHERE id != alumno_preservado_id;
    ELSE
        -- Si no existe 20224106A, eliminar todo
        DELETE FROM log_ciclo_relativo;
        DELETE FROM log_creditos;
        DELETE FROM matricula;
        DELETE FROM usuario WHERE alumno_id IS NOT NULL;
        DELETE FROM alumno;
    END IF;
END $$;

